// Add typing effect to hero title
function typeWriter(element, text, speed = 100) {
    let i = 0;
    element.innerHTML = '';
    
    function type() {
        if (i < text.length) {
            element.innerHTML += text.charAt(i);
            i++;
            setTimeout(type, speed);
        }
    }
    
    type();
}

// Video Modal Functions
function openVideoModal(videoId, clientName, clientQuote) {
    const modal = document.getElementById('videoModal');
    const modalClientName = document.getElementById('modalClientName');
    const modalClientQuote = document.getElementById('modalClientQuote');
    const modalVideoFrame = document.getElementById('modalVideoFrame');
    
    modalClientName.textContent = clientName;
    modalClientQuote.textContent = `"${clientQuote}"`;
    modalVideoFrame.src = `https://drive.google.com/file/d/${videoId}/preview`;
    
    modal.style.display = 'block';
    document.body.style.overflow = 'hidden';
}

function closeVideoModal() {
    const modal = document.getElementById('videoModal');
    const modalVideoFrame = document.getElementById('modalVideoFrame');
    
    modal.style.display = 'none';
    modalVideoFrame.src = '';
    document.body.style.overflow = 'auto';
}

// Modal event listeners
document.addEventListener('DOMContentLoaded', () => {
    const modal = document.getElementById('videoModal');
    const closeBtn = document.querySelector('.video-modal-close');
    
    // Close modal when clicking X
    if (closeBtn) {
        closeBtn.addEventListener('click', closeVideoModal);
    }
    
    // Close modal when clicking outside
    if (modal) {
        modal.addEventListener('click', function(e) {
            if (e.target === modal) {
                closeVideoModal();
            }
        });
    }
    
    // Close modal with Escape key
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape') {
            closeVideoModal();
        }
    });
});

